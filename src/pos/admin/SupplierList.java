    private void removeSupplierBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeSupplierBtnActionPerformed
        int[] rows = supplierTable.getSelectedRows();
        boolean[] rowsToRemoveFromList = new boolean[rows.length];
        for (int rowNum = 0; rowNum < rows.length; rowNum++) {
            Object toRemoveId = supplierTable.getValueAt(rowNum, 0);
            rowsToRemoveFromList[rowNum] = Supplier.remove(mainWindow.getDbManager(), toRemoveId);
        }
        //remove listed as true
        for (int i = rowsToRemoveFromList.length - 1; i >= 0; i--) {
            if (rowsToRemoveFromList[i]) {
                ((DefaultTableModel) supplierTable.getModel()).removeRow(rows[i]);
            }
        }
    }//GEN-LAST:event_removeSupplierBtnActionPerformed

